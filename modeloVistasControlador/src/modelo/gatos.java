package modelo;

public class gatos {
int id;
String raza;
String nombre;
String foto;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getRaza() {
	return raza;
}
public void setRaza(String raza) {
	this.raza = raza;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public String getFoto() {
	return foto;
}
public void setFoto(String foto) {
	this.foto = foto;
}
}
