package modelo.vo;

public class SucursalesVo {

	private int suSucarsal;
	private String suCiudad;
	private int suActivo;
	public int getSuSucarsal() {
		return suSucarsal;
	}
	public void setSuSucarsal(int suSucarsal) {
		this.suSucarsal = suSucarsal;
	}
	public String getSuCiudad() {
		return suCiudad;
	}
	public void setSuCiudad(String suCiudad) {
		this.suCiudad = suCiudad;
	}
	public int getSuActivo() {
		return suActivo;
	}
	public void setSuActivo(int suActivo) {
		this.suActivo = suActivo;
	}
	
}
