package Ejemplos;

public class Asignaciones {

	public static void main(String[] args) {
		int a=5, b=2;
		a+=b;
		System.out.println("A vale aghora "+a); //7
		a-=b;
		System.out.println("A vale aghora "+a);//5
		a*=b;
		System.out.println("A vale aghora "+a);//10
		a%=b;
		System.out.println("A vale aghora "+a);//0
	}
}
